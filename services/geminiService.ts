import { GoogleGenAI } from "@google/genai";

const ai = new GoogleGenAI({ apiKey: process.env.API_KEY });

export const generateWolfyLogo = async (): Promise<string | null> => {
  try {
    const response = await ai.models.generateContent({
      model: 'gemini-2.5-flash-image',
      contents: {
        parts: [
          {
            text: 'A circular logo for "Wolfy\'s Websites". Illustration of a cool cartoon wolfman character wearing a casual t-shirt and blue jeans, sitting at a desk typing on a modern computer. The style is professional vector art, clean lines, white background. Friendly but tech-savvy vibe.',
          },
        ],
      },
      config: {
        imageConfig: {
          aspectRatio: "1:1",
        }
      }
    });

    for (const part of response.candidates?.[0]?.content?.parts || []) {
      if (part.inlineData) {
        return `data:image/png;base64,${part.inlineData.data}`;
      }
    }
    return null;
  } catch (error) {
    console.error("Error generating Wolfy logo:", error);
    return null;
  }
};

